#!/usr/bin/env node
// One-shot scraper: fetches https://www.weather.gov/help-map/, extracts the
// event → hex color legend, precomputes WCAG 3:1 contrast tags against HA
// light (#ffffff) and dark (#1c1c1e) card backgrounds, writes src/nws-colors.ts.
//
// Run manually when NWS updates the legend:
//   node scripts/generate-nws-colors.mjs
//
// Output is committed to the repo. Not wired into the build.

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const SOURCE_URL = 'https://www.weather.gov/help-map/';
const LIGHT_BG = '#ffffff';
const DARK_BG = '#1c1c1e';
const CONTRAST_THRESHOLD = 3.0;

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '..', 'src', 'nws-colors.ts');

function hexToRgb(hex) {
  const h = hex.replace('#', '');
  return [
    parseInt(h.slice(0, 2), 16),
    parseInt(h.slice(2, 4), 16),
    parseInt(h.slice(4, 6), 16),
  ];
}

function relativeLuminance(hex) {
  const [r, g, b] = hexToRgb(hex).map(c => c / 255);
  const lin = (c) => c <= 0.04045 ? c / 12.92 : ((c + 0.055) / 1.055) ** 2.4;
  return 0.2126 * lin(r) + 0.7152 * lin(g) + 0.0722 * lin(b);
}

function contrastRatio(hexA, hexB) {
  const la = relativeLuminance(hexA);
  const lb = relativeLuminance(hexB);
  const light = Math.max(la, lb);
  const dark = Math.min(la, lb);
  return (light + 0.05) / (dark + 0.05);
}

async function fetchLegend() {
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`Fetch failed: ${res.status} ${res.statusText}`);
  return res.text();
}

function parseLegend(html) {
  // Row shape inside <tbody>: <tr>
  //   <td ...><span ...>{EVENT}</span></td>
  //   <td ...><span ...>{PRIORITY}</span></td>
  //   <td id="z{HEX}" style="background-color:#{HEX}">...
  // Split by <tr> so header-row <span>s can't leak into data-row matches.
  const rows = html.split(/<tr[^>]*>/);
  const entries = [];
  for (const row of rows) {
    const end = row.indexOf('</tr>');
    if (end === -1) continue;
    const rowContent = row.slice(0, end);
    // First <td><span>...</span></td> holds the event name; skip <th> cells.
    const eventMatch = /<td[^>]*>\s*<span[^>]*>([^<]+)<\/span>/.exec(rowContent);
    const colorMatch = /background-color:#([0-9A-Fa-f]{6})/.exec(rowContent);
    if (eventMatch && colorMatch) {
      entries.push({ event: eventMatch[1].trim(), hex: `#${colorMatch[1].toUpperCase()}` });
    }
  }
  return entries;
}

function render(entries) {
  const lines = [];
  lines.push('// Auto-generated by scripts/generate-nws-colors.mjs. Do not edit by hand.');
  lines.push(`// Source: ${SOURCE_URL}`);
  lines.push(`// Generated: ${new Date().toISOString().slice(0, 10)}`);
  lines.push(`// Boost tags: contrast < ${CONTRAST_THRESHOLD.toFixed(1)}:1 vs card background`);
  lines.push(`//   boostLight = fails against ${LIGHT_BG}`);
  lines.push(`//   boostDark  = fails against ${DARK_BG}`);
  lines.push('');
  lines.push('export interface NwsColorEntry {');
  lines.push('  hex: string;');
  lines.push('  rgb: string;');
  lines.push('  boostLight: boolean;');
  lines.push('  boostDark: boolean;');
  lines.push('}');
  lines.push('');
  lines.push('export const NWS_EVENT_COLORS: Record<string, NwsColorEntry> = {');

  const seen = new Set();
  for (const { event, hex } of entries) {
    const key = event.toLowerCase();
    if (seen.has(key)) continue;
    seen.add(key);
    const rgb = hexToRgb(hex).join(', ');
    const boostLight = contrastRatio(hex, LIGHT_BG) < CONTRAST_THRESHOLD;
    const boostDark = contrastRatio(hex, DARK_BG) < CONTRAST_THRESHOLD;
    lines.push(`  '${key}': { hex: '${hex}', rgb: '${rgb}', boostLight: ${boostLight}, boostDark: ${boostDark} },`);
  }

  lines.push('};');
  lines.push('');
  return lines.join('\n');
}

async function main() {
  const html = await fetchLegend();
  const entries = parseLegend(html);
  if (entries.length < 50) {
    throw new Error(`Parsed only ${entries.length} entries — page structure may have changed`);
  }
  const output = render(entries);
  writeFileSync(outPath, output, 'utf-8');
  const light = entries.filter(e => contrastRatio(e.hex, LIGHT_BG) < CONTRAST_THRESHOLD).length;
  const dark = entries.filter(e => contrastRatio(e.hex, DARK_BG) < CONTRAST_THRESHOLD).length;
  console.log(`Wrote ${outPath}`);
  console.log(`  ${entries.length} entries`);
  console.log(`  ${light} fail 3:1 vs light (${LIGHT_BG}) → boostLight: true`);
  console.log(`  ${dark} fail 3:1 vs dark (${DARK_BG}) → boostDark: true`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
